    private void txtNombre_agregarClienteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombre_agregarClienteKeyTyped
        // TODO add your handling code here:
        
        int key = evt.getKeyChar();

    boolean mayusculas = key >= 65 && key <= 90;
    boolean minusculas = key >= 97 && key <= 122;
    boolean espacio = key == 32;
            
     if (!(minusculas || mayusculas || espacio))
    {
        evt.consume();
    }
    }//GEN-LAST:event_txtNombre_agregarClienteKeyTyped

