    private void btnGuardar_agregarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardar_agregarClienteActionPerformed

        Long dni = Long.parseLong(txtDni_agregarCliente.getText());
        String nom = txtNombre_agregarCliente.getText();
        String ape = txtApellido_agregarCliente.getText();
        String dir = txtDireccion_agregarCliente.getText();
        String ciu = txtCiudad_agregarCliente.getText();

        Cliente cli = new Cliente(dni, nom, ape, ciu, dir);

        Long tel = Long.parseLong(txtTelefono_agregarCliente.getText());

        boolean ex = MenuPrincipal.directorio.agregarCliente(tel, cli);
        if (!ex) {
            JOptionPane.showMessageDialog(this, "Este nro de telefono ya existe");

        } else {
            JOptionPane.showMessageDialog(this, " El cliente se ha guardado con éxito!");
        }

    }//GEN-LAST:event_btnGuardar_agregarClienteActionPerformed

